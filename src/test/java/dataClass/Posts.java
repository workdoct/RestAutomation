package dataClass;

public class Posts {
	 private String id;
	 private String title;
	 private String author;
	 
	 public void setID(String id) {
		 this.id = id;
	 }

	 public void setTitle(String title) {
		 this.title = title;
	 }
	 
	 public void setAuthor(String author) {
		 this.author = author;
	 }
	 
	 public String getID() {
		 return id;
	 } 
	 
	 public String getAuthor() {
		 return author;
	 } 
	 
	 public String getTitle() {
		 return title;
	 } 
	 
}